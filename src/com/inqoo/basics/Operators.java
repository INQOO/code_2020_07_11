package com.inqoo.basics;

public class Operators {

    public static void main(String[] args) {
        System.out.println(5 + 5); // dodawanie
        System.out.println(50 - 5); // odejmowanie
        System.out.println(6 * 6); // mnożenie
        System.out.println(100 / 20); // dzielenie
        System.out.println(10 % 2); // reszta z dzielenia
        System.out.println(2 + 2 * 3);

        System.out.println("================");

        int a = 10;
        int b = 15;
        System.out.println(a == b); // sprawdzenie równości
        System.out.println(10 > 2); // większe
        System.out.println(100 <= 100); // mniejsze lub równe
        System.out.println(a != b); // różne

        System.out.println("================");

        System.out.println(false || true);
        System.out.println(true && true);

    }
}
